import UIKit

extension UIColor {
    class func vaultGrey() -> UIColor {
        return UIColor(red: 138, green: 165, blue: 190, alpha: 1)
    }
    class func backgroundDark() -> UIColor {
        return UIColor(red: 24/255, green: 27/255, blue: 35/255, alpha: 1)
    }
    class func barButton() -> UIColor {
        return UIColor(red: 42/255, green: 46/255, blue: 57/255, alpha: 1)
    }
    class func navigationBar() -> UIColor {
        return UIColor(red: 32/255, green: 36/255, blue: 47/255, alpha: 1)
    }
    class func textLightGrey() -> UIColor {
        return UIColor(red: 140/255, green: 151/255, blue: 176/255, alpha: 1)
    }
    class func textDarkGrey() -> UIColor {
        return UIColor(red: 108/255, green: 123/255, blue: 154/255, alpha: 1)
    }
}
